SOS_token = 0
EOS_token = 1


class Lang:
    def __init__(self):
        self.char2index = {}
        self.char2count = {}
        self.index2char = {0: "SOS", 1: "EOS"}
        self.n_chars = 2  # Count SOS and EOS

    def addSentence(self, sentence):
        """
        Input is a list of chars, with " " as a word delimiter,
        and full tags at the end of the list
        """
        for i, char in enumerate(sentence):
            if char == " ":
                #replace empty space (word delimiter) with "#"
                char = "#"
            # And then use addWord to add all chars in that word
            self.addChar(char)

    def addChar(self, char):
        if char not in self.char2index:
            self.char2index[char] = self.n_chars
            self.char2count[char] = 1
            self.index2char[self.n_chars] = char
            self.n_chars += 1
        else:
            self.char2count[char] += 1


